     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $accounts = $this->validate(request(), [
            'userid' => 'required',
            'usertype' => 'required',
            'title' => 'required',
            'firstname' => 'required',
            'lastname' => 'required',
            'email' => 'required',
            'password' => 'required' 
        ]);
          
          Accounts::create($accounts);
  
          return back()->with('success', 'Account has been added');
    }

