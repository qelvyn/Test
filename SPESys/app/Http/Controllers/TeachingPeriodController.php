    {
        $teachingPeriod = $this->validate(request(), [
            'teachingPeriodName' => 'required',
            'location' => 'required',
            'startDate' => 'required',
            'endDate' => 'required'
          ]);
          
          TeachingPeriod::create($teachingPeriod);
  
          return back()->with('success', 'A Teaching Period has been added');;
